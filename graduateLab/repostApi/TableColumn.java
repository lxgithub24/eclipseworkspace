package repostApi;

public class TableColumn {
	private String name;
	private int age;
	private int xi;
	public void setName(String name){
		this.name = name;
	}
	public String getName(){
		return this.name;
	}
	public void setAge(int age){
		this.age = age;
	}
	public int getAge(){
		return age;
	}
	public void setXi(int xi){
		this.xi = xi;
	}
	public int getXi(){
		return xi;
	}
}
